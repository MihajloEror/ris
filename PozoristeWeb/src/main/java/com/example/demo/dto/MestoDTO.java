package com.example.demo.dto;

public class MestoDTO {

	private String sekcija;
	private Integer red;
	private Integer broj;
	private String nazivScene;
	public String getSekcija() {
		return sekcija;
	}
	public void setSekcija(String sekcija) {
		this.sekcija = sekcija;
	}
	public Integer getRed() {
		return red;
	}
	public void setRed(Integer red) {
		this.red = red;
	}
	public Integer getBroj() {
		return broj;
	}
	public void setBroj(Integer broj) {
		this.broj = broj;
	}
	public String getNazivScene() {
		return nazivScene;
	}
	public void setNazivScene(String nazivScene) {
		this.nazivScene = nazivScene;
	}
	
	
}
